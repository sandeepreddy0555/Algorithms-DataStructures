  private static boolean areFollowingPatterns(String[] strings, String[] patterns) {
    Map<String, String> patterMap = new HashMap<>();

    for (int i = 0; i < patterns.length; i++) {
      if (patterMap.containsKey(patterns[i])) {
        if (!patterMap.get(patterns[i]).equals(strings[i])) {
          return false;
        }
      } else {
        if (patterMap.containsValue(strings[i])) {
          return false;
        }
        patterMap.put(patterns[i], strings[i]);
      }
    }
    return true;
  }

